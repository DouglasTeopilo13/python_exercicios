carros = ['bmw', 'audi', 'mercedes', 'toyota', 'honda']
carros.sort()  # Ordena a lista de carros em ordem alfabética
print(carros)

carros.sort(reverse=True) # Ordena a lista de carros em ordem alfabética reversa
print(carros)

